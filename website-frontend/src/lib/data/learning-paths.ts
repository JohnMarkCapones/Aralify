// Helper to get courses grouped into phases
export function getCoursesInPhases(path: LearningPath): RoadmapPhase[] {
  const courses = [...path.courses];
  const phaseCount = path.phases.length;

  if (phaseCount === 0) return [];

  const coursesPerPhase = Math.ceil(courses.length / phaseCount);

  return path.phases.map((phase, i) => ({
    ...phase,
    courses: courses.slice(
      i * coursesPerPhase,
      Math.min((i + 1) * coursesPerPhase, courses.length)
    ),
  }));
}
